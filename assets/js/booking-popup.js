function openpopup()
{
    var x=document.getElementById('booking-popup');
    var y=document.getElementById('blur-bg');
    x.style.display="block";
    y.style.display="block";
}
function closepopup()
{
    var x=document.getElementById('booking-popup');
    var y=document.getElementById('blur-bg');
    x.style.display="none";
    y.style.display="none";
}